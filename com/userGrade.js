var gradeNum = function(value) {
	var index = 0;
	if(!isNaN(value)) {
		if(value < 1) {
			value = 1;
		} else if(value > 300) {
			value = 300;
		}
		if(value % 10 == 0) {
			value -= 1;
		}
		index = Math.floor(value / 10);
	}
	return index;
};

var binding = function(el, obj, type) {
	var index = gradeNum(obj.value);;
	
	if(type == "img") {
		el.style.backgroundImage = "url("+ userGrade.gradeImg[index] +")";
	} else {
		el.style.backgroundImage = "url("+ userGrade.grade[index] +")";
	}
}

Vue.directive("usergrade", {
	bind: function(el, obj) {
		binding(el, obj);
	},
	update: function(el, obj){
		binding(el, obj);
	}
});

Vue.directive("usergradeimg", {
	bind: function(el, obj) {
		binding(el, obj, "img");
	},
	update: function(el, obj){
		binding(el, obj, "img");
	}
});